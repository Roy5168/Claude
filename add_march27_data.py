def update_json(filename, new_entries):
    path = os.path.join(DATA_DIR, filename)
    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)

    # Check if entries for these dates already exist; avoid duplicates
    existing_dates = {r.get("日期") for r in data}
    new_dates = {r.get("日期") for r in new_entries}
    to_add = [r for r in new_entries if r.get("日期") not in existing_dates or True]

    # Filter: only add entries for dates not yet in file
    filtered = [r for r in new_entries if r.get("日期") not in existing_dates]
    if not filtered:
        print(f"  {filename}: 所有日期已存在，無需更新")
        return

    data.extend(filtered)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    added_dates = sorted({r.get("日期") for r in filtered})
    print(f"  {filename}: 新增 {len(filtered)} 筆（日期: {', '.join(added_dates)}）")


