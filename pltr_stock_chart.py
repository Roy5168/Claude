def build_dataframe():
    """Build a pandas DataFrame from the hardcoded PLTR data."""
    df = pd.DataFrame(
        _PLTR_DATA,
        columns=["Date", "Open", "High", "Low", "Close", "Volume"],
    )
    df["Date"] = pd.to_datetime(df["Date"])
    df.set_index("Date", inplace=True)
    df.index.name = "Date"
    return df


def calculate_moving_averages(df):
    """Add 5, 20, 60-day simple moving averages."""
    df["SMA_5"]  = df["Close"].rolling(window=5).mean()
    df["SMA_20"] = df["Close"].rolling(window=20).mean()
    df["SMA_60"] = df["Close"].rolling(window=60).mean()
    return df


def plot_stock_chart(df, ticker="PLTR", save_path="pltr_chart.png"):
    """Render and save the candlestick chart with MA overlays."""
    apds = [
        mpf.make_addplot(df["SMA_5"],  color="#1f77b4", width=1.5, label="SMA 5"),
        mpf.make_addplot(df["SMA_20"], color="#ff7f0e", width=1.5, label="SMA 20"),
        mpf.make_addplot(df["SMA_60"], color="#d62728", width=1.5, label="SMA 60"),
    ]

    # Custom style
    mc = mpf.make_marketcolors(
        up="limegreen", down="tomato",
        wick={"up": "limegreen", "down": "tomato"},
        volume={"up": "limegreen", "down": "tomato"},
        edge="inherit",
    )
    style = mpf.make_mpf_style(
        marketcolors=mc,
        gridstyle="--",
        gridcolor="#dddddd",
        facecolor="#f8f9fa",
        figcolor="#f8f9fa",
        y_on_right=True,
    )

    fig, axes = mpf.plot(
        df,
        type="candle",
        style=style,
        title=f"\n{ticker}  |  Candlestick + SMA(5/20/60)",
        ylabel="Price (USD)",
        volume=True,
        addplot=apds,
        figsize=(16, 9),
        panel_ratios=(3, 1),
        warn_too_much_data=len(df) + 1,
        tight_layout=True,
        returnfig=True,
    )

    # Add legend manually (mplfinance addplot labels aren't auto-shown)
    ax_main = axes[0]
    legend_lines = [
        mpf.make_addplot(df["SMA_5"],  color="#1f77b4", width=1.5),
        mpf.make_addplot(df["SMA_20"], color="#ff7f0e", width=1.5),
        mpf.make_addplot(df["SMA_60"], color="#d62728", width=1.5),
    ]
    import matplotlib.lines as mlines
    handles = [
        mlines.Line2D([], [], color="#1f77b4", linewidth=1.5, label="SMA 5"),
        mlines.Line2D([], [], color="#ff7f0e", linewidth=1.5, label="SMA 20"),
        mlines.Line2D([], [], color="#d62728", linewidth=1.5, label="SMA 60"),
    ]
    ax_main.legend(handles=handles, loc="upper left", fontsize=10, framealpha=0.8)

    fig.savefig(save_path, dpi=150, bbox_inches="tight")
    print(f"Chart saved → {save_path}")


def main():
    df = build_dataframe()
    df = calculate_moving_averages(df)

    print(f"Ticker       : PLTR")
    print(f"Data range   : {df.index[0].date()} → {df.index[-1].date()}")
    print(f"Trading days : {len(df)}")
    print(f"\nLatest values (as of {df.index[-1].date()}):")
    print(f"  Close    : ${df['Close'].iloc[-1]:.2f}")
    print(f"  SMA  5   : ${df['SMA_5'].iloc[-1]:.2f}")
    print(f"  SMA 20   : ${df['SMA_20'].iloc[-1]:.2f}")
    sma60 = df['SMA_60'].iloc[-1]
    print(f"  SMA 60   : ${sma60:.2f}" if not np.isnan(sma60) else "  SMA 60   : N/A")

    print("\nGenerating chart...")
    plot_stock_chart(df, "PLTR", save_path="pltr_chart.png")
    print("Done!")


if __name__ == "__main__":
    main()
